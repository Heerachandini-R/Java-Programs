package com.java.heera;

public class OperatorTest {

	public static void main(String[] args) {
		System.out.println(10/3);
		System.out.println(10%3);
		System.out.println(-10%3);
		System.out.println(10%-3);
		System.out.println(-10%-3);
		System.out.println(10.0/3.0);
		System.out.println(10.0/0);
		System.out.println(-10.0/0);
		System.out.println(10/0);

	}

}
